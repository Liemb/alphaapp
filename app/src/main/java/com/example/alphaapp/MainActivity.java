package com.example.alphaapp;

import androidx.annotation.NonNull;
import androidx.appcompat.app.AppCompatActivity;

import android.content.Intent;
import android.os.Bundle;
import android.view.Menu;
import android.view.MenuItem;
import android.view.View;
import android.widget.EditText;
import android.widget.Toast;

import com.google.android.gms.tasks.OnCompleteListener;
import com.google.android.gms.tasks.Task;
import com.google.firebase.auth.AuthResult;
import com.google.firebase.auth.FirebaseAuth;
import com.google.firebase.auth.FirebaseUser;

/**
 * @author liem
 * @since 18/10/2020
 */

public class MainActivity extends AppCompatActivity {

    private FirebaseAuth mAuth;
    EditText uname, upassword;

    @Override
    protected void onCreate(Bundle savedInstanceState) {
        super.onCreate(savedInstanceState);
        setContentView(R.layout.activity_main);

        mAuth = FirebaseAuth.getInstance();
        uname = (EditText) findViewById(R.id.uname);
        upassword = (EditText) findViewById(R.id.upassword);
    }

    @Override
    public void onStart() {
        super.onStart();
        // Check if user is signed in (non-null) and update UI accordingly.
        FirebaseUser currentUser = mAuth.getCurrentUser();
        updateUI(currentUser);
    }

    private void updateUI(FirebaseUser currentUser) {
    }

    /**
     * registering new accounts
     *
     * @param view
     */
    public void signing(View view) {
        String sname = uname.getText().toString();
        String spassword = upassword.getText().toString();
        mAuth.createUserWithEmailAndPassword(sname, spassword)
                .addOnCompleteListener(this, new OnCompleteListener<AuthResult>() {
                    @Override
                    public void onComplete(@NonNull Task<AuthResult> task) {
                        if (task.isSuccessful()) {
                            // Sign in success, update UI with the signed-in user's information
                            FirebaseUser user = mAuth.getCurrentUser();
                            updateUI(user);
                            Toast.makeText(MainActivity.this, "registered!",
                                    Toast.LENGTH_SHORT).show();
                        } else {
                            // If sign in fails, display a message to the user.
                            Toast.makeText(MainActivity.this, "Authentication failed.",
                                    Toast.LENGTH_SHORT).show();
                            updateUI(null);
                        }

                        // ...
                    }


                });
    }

    /**
     * inflate the menu
     *
     * @param menu
     * @return
     */
    public boolean onCreateOptionsMenu(Menu menu) {
        getMenuInflater().inflate(R.menu.main, menu);
        return true;
    }

    public boolean onOptionsItemSelected (MenuItem item){
        String st = item.getTitle().toString();

        if (st.equals("location")){
            Intent si = new Intent(this, locations.class);
            startActivity(si);
        }

        if (st.equals("mail")) {
            Intent si = new Intent(this, mails.class);
            startActivity(si);
        }

        if (st.equals("camera")) {
            Intent si = new Intent(this, scamera.class);
            startActivity(si);
        }

        if (st.equals("csv")) {
            Intent si = new Intent(this, csv.class);
            startActivity(si);
        }

        return true;
    }
}